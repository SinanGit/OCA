package Basics.StringMethods;

public class charAt {
    public static void main(String[] args) {
        String name = "Sinan Yorulmaz";
        char charAt8 = name.charAt(8);
        System.out.println("charAt8 = " + charAt8);
        char charAtLastname = name.charAt(10);
        System.out.println("charAtLastname = " + charAtLastname);
    }
}
